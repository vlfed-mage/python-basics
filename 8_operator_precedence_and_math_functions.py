# round()
x = 2.9
print(round(x))  # 3

# abs()
x = -2.9
print(abs(x))  # 2.9

# math.ceil()
x = 2.9
print(math.ceil(x))  # 3

# math.floor()
x = 2.9
print(math.floor(x))  # 2
